	#include <iostream>
	#include <string>
	#include "DUPoint.h"
	#include "DULine.h"
	using namespace std ;


//----------------------------------------------------------------------------
//
//-------------------------------- Prototypes --------------------------------
//
//----------------------------------------------------------------------------

	int  min_value  ( int &a, int &b ) ;
	int& min_element( int &a, int &b ) ;
	
//----------------------------------------------------------------------------
//
//------------------------------- Main Program -------------------------------
//
//----------------------------------------------------------------------------

	int main(void)
	{

		int i = 5, k = 7;
	
	//----------------------------------------------------------------------------
	// iPtr is a variable that can point to an integer
	//----------------------------------------------------------------------------
		int * iPtr ;	
					
	//----------------------------------------------------------------------------
	// iPtr is assigned the address of i
	// iPtr now "points at" i
	//----------------------------------------------------------------------------
		iPtr = &i ;
	
	//----------------------------------------------------------------------------
	// Printing iPtr reveals the address of i
	//----------------------------------------------------------------------------
		cout << "iPtr=" << iPtr << endl ;
	
	//----------------------------------------------------------------------------
	// The object pointed at by iPtr may be referenced
	// as *iPtr
	//----------------------------------------------------------------------------
		cout << "*iPtr=" << *iPtr << endl ;
		cout << "i=" << i << endl ;
	
	//----------------------------------------------------------------------------
	// jPtr is another 'integer pointer' variable
	//----------------------------------------------------------------------------
		int * jPtr ;
	
	//----------------------------------------------------------------------------
	// jPtr is assigned the value of iPtr
	// jPtr now holds the same address that iPtr holds
	//----------------------------------------------------------------------------
        int temp;
        jPtr = &temp ;
		cout << "jPtr=" << jPtr << endl ;
        cout << "*jPtr="<<*jPtr <<endl;
	
        int * kPtr;
        
        kPtr=&k;
        
        cout << "kPtr="<< kPtr<<endl;
        cout << "*kPtr="<< *kPtr <<endl;
        
        cout<<i<<" "<<k<<endl;
        
        *jPtr = *iPtr;
        
        *iPtr = *kPtr;
        
        *kPtr = *jPtr;
        
        cout<<i<<" "<<k<<endl;
        
	    return 0;
	}


//----------------------------------------------------------------------------
//
//-------------------------- Subprogram Definitions --------------------------
//
//----------------------------------------------------------------------------

	int  min_value  ( int &a, int &b )
	{
		if( a < b )
			return a ;
		else
			return b ;
	}
	
//----------------------------------------------------------------------------
//
	int& min_element( int &a, int &b )
	{
		if( a < b )
			return a ;
		else
			return b ;
	}